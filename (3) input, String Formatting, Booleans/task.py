#** Create Simple Calculator, Take 2 Numbers From User Then Do The Simple 4 operations[+,-,*,/].

num1 = float(input("[?] Enter The First Number: "))
num2 = float(input("[?] Enter The Second Number: "))

sum2Nums = num1+num2
sub2Nums = num1-num2
mult2Nums = num1*num2
div2Nums = num1/num2
print('========= Result =========')
print("[+] Sum Operation: "+str(sum2Nums))
print("[-] Sub Operation: "+str(sub2Nums))
print("[*] Multiply Operation: "+str(mult2Nums))
print("[/] Division Operation: "+str(div2Nums))


